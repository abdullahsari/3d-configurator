import Configurator from './Configurator';

{
    'use strict';

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GLOBALLY AVAILABLE //////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    const config = new Configurator();

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CACHE ///////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    const panelBtns = document.getElementsByClassName('btn');

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // EVENT LISTENERS /////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    // Panel buttons
    for (let button of panelBtns) {
        button.addEventListener('click', () => {
            togglePanel(button.parentNode);
        });
    }

    const togglePanel = targetPanel => {
        targetPanel.classList.toggle('open');
    };

    // Everything is set, render!
    config.render();
}